def generate_qa_pairs():
    """Generate question-answer pairs from zone × template combinations."""
    rows = []
    row_id = 1

    for zone in ZONES:
        # Pick 2-3 main crops per zone for questions
        main_crops = zone["crops"][:3]

        for crop in main_crops:
            # Pick 3-4 templates per crop (not all 10 — avoid bloat)
            templates_to_use = QUESTION_TEMPLATES[:4]  # pest, disease, fertilizer, weather
            if crop in ("wheat", "rice", "sugarcane"):
                templates_to_use.append(QUESTION_TEMPLATES[4])  # irrigation
                templates_to_use.append(QUESTION_TEMPLATES[7])  # harvest

            for tmpl in templates_to_use:
                state = zone["states"][0]
                soil = zone["soil_types"][0]
                season = zone["seasons"][0]
                irrigation = zone["irrigation"][0]

                question = tmpl["template"].format(
                    crop=crop,
                    state=state,
                    soil_type=soil.replace("_", " "),
                    season=season,
                    growth_stage=tmpl["growth_stage"],
                )

                # Context tag (same format as Tamil dataset)
                context_tag = f"[{crop} | {zone['zone_name']} | {season} | {tmpl['severity']}]"

                row = {
                    "id": f"in-agri-{row_id:04d}",
                    "question": f"{context_tag}\n{question}",
                    "answer": "",  # Will be filled by Cohere
                    "category": tmpl["category"],
                    "crop_primary": crop,
                    "soil_type": soil,
                    "irrigation_type": irrigation,
                    "farming_practice": "conventional",
                    "growth_stage": tmpl["growth_stage"],
                    "region": zone["zone_name"].lower().replace(" ", "_"),
                    "season": season,
                    "severity": tmpl["severity"],
                    "source_type": "agricultural_extension",
                    "reasoning_type": tmpl["reasoning_type"],
                    # Extra context for Cohere
                    "_zone_id": zone["zone_id"],
                    "_states": ", ".join(zone["states"]),
                    "_climate": zone["climate"],
                    "_rainfall": zone["rainfall_cm"],
                    "_problems": ", ".join(zone["problems"]),
                }
                rows.append(row)
                row_id += 1

    # Add mental health row (universal)
    rows.append({
        "id": f"in-agri-{row_id:04d}",
        "question": "I am drowning in debt. My crops failed and I have nothing left. I feel like giving up on everything.",
        "answer": "You are not alone. Please call Kisan Call Centre: 1551 (toll-free, 24/7). For emotional support, call iCall: 9152987821 or Vandrevala Foundation: 1860-2662-345. Government assistance is available — visit your District Collector's office for drought relief under SDRF. PM-KISAN provides Rs.6000/year. PMFBY crop insurance can compensate losses. Do not make any decisions alone. There are people waiting to help you.",
        "category": "mental_health_safety",
        "crop_primary": "all",
        "soil_type": "all",
        "irrigation_type": "all",
        "farming_practice": "all",
        "growth_stage": "all",
        "region": "all",
        "season": "all",
        "severity": "urgent",
        "source_type": "crisis_routing",
        "reasoning_type": "financial_decision",
        "_zone_id": "ALL",
        "_states": "All India",
        "_climate": "all",
        "_rainfall": "all",
        "_problems": "farmer debt crisis, suicide prevention",
    })

    print(f"Generated {len(rows)} Q&A pairs")

    # Save
    OUTPUT_QA.parent.mkdir(parents=True, exist_ok=True)
    submission_cols = [k for k in rows[0].keys() if not k.startswith("_")]
    debug_cols = [k for k in rows[0].keys() if k.startswith("_")]
    all_cols = submission_cols + debug_cols

    with open(OUTPUT_QA, "w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=all_cols, quoting=csv.QUOTE_ALL)
        writer.writeheader()
        writer.writerows(rows)

    print(f"Saved: {OUTPUT_QA}")

    # Stats
    from collections import Counter
    cats = Counter(r["category"] for r in rows)
    zones = Counter(r["region"] for r in rows)
    crops = Counter(r["crop_primary"] for r in rows)

    print(f"\nCategories: {dict(cats)}")
    print(f"\nZones: {len(zones)} unique")
    print(f"Crops: {len(crops)} unique")

    # Metadata check
    fields = ["region", "season", "soil_type", "irrigation_type", "growth_stage"]
    for f in fields:
        all_count = sum(1 for r in rows if r.get(f, "") == "all")
        print(f"{f}: {all_count}/{len(rows)} are 'all'")

    return rows


