"""
19_build_india_dataset.py
--------------------------
Generates an India-wide agricultural advisory dataset using:
  1. India's 15 agro-climatic zones (Planning Commission) as the metadata backbone
  2. Question patterns from the Tamil dataset (proven Grade A structure)
  3. Crop-soil-season mappings from ICAR/NBSS data
  4. Cohere to generate Hindi/English advisory answers in 5-part structure

The output is a submission-ready CSV for the Adaption platform.

Strategy:
  - 8-10 zones × 8-10 questions per zone = 80-100 rows
  - Each row has REAL metadata: zone, state, soil, crops, season, irrigation
  - Questions cover the same 10 categories that scored Grade A in Tamil
  - Answers generated by Cohere in 5-part ICAR extension officer format

Run from repo root:
    python scripts/19_build_india_dataset.py --generate     # generate Q&A pairs
    python scripts/19_build_india_dataset.py --expand        # expand with Cohere
    python scripts/19_build_india_dataset.py --expand --limit 5  # test on 5
"""

